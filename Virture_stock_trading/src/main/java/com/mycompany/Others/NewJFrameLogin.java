    private JSONArray readJsonFile() {
        JSONParser parser = new JSONParser();
        JSONArray json = null;
        try {
            FileReader reader = new FileReader("users.json");
            json = (JSONArray) parser.parse(reader);
            reader.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return json;
    }//GEN-LAST:event_loginbtnActionPerformed

