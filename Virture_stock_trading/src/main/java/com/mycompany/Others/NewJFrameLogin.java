    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        JSONArray json = readJsonFile();
        if (json == null) {
            JOptionPane.showMessageDialog(this, "users.json 파일을 읽어오지 못했습니다.", "에러", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String id = idtext.getText();
        String password = new String(jPasswordField1.getPassword());

        // JSONArray에서 id와 password가 일치하는 객체를 찾습니다.
        JSONObject user = null;
        for (int i = 0; i < json.size(); i++) {
            JSONObject obj = (JSONObject) json.get(i);
            if (obj.get("id").equals(id) && obj.get("password").equals(password)) {
                user = obj;
                break;
            }
        }

        if (user == null) {
            JOptionPane.showMessageDialog(this, "아이디 또는 비밀번호가 일치하지 않습니다.", "알림", JOptionPane.WARNING_MESSAGE);
            return;
        }

        JOptionPane.showMessageDialog(this, "로그인 성공!", "알림", JOptionPane.INFORMATION_MESSAGE);
        new stock_search().setVisible(true);
    }

